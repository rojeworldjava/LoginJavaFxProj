    private void jSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSubmitButtonActionPerformed
        // TODO add your handling code here:
          if(jUserNameField.getText().toLowerCase().replaceAll("\\s+","").equals("username")   &&  (jPasswordField.getText().toLowerCase().replaceAll("\\s+","").equals("password") || jPasswordField.getText().replaceAll("\\s+","").equals("")))
        {
            JOptionPane.showConfirmDialog(this, "Please Enter Your User Name and Password", "Login - Message",JOptionPane.CLOSED_OPTION );
            
        }
        else  if(jUserNameField.getText().toLowerCase().replaceAll("\\s+","").equals("username"))
        {
            JOptionPane.showConfirmDialog(this, "Please Enter Your User Name", "Login - Message",JOptionPane.CLOSED_OPTION);
            jUserNameField.grabFocus();
        }
        else if(jPasswordField.getText().toLowerCase().replaceAll("\\s+","").equals("password") || jPasswordField.getText().replaceAll("\\s+","").equals("") )
        {
              JOptionPane.showConfirmDialog(this, "Please Enter Your Password", "Login - Message",JOptionPane.CLOSED_OPTION);
              jPasswordField.grabFocus();
        }
        else{
             String sql="select * from login where username = ? and password = ?";
        
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn= DriverManager.getConnection("jdbc:mysql://localhost:3306/employe","root" , "1234");
            
            
            PreparedStatement pstm=conn.prepareStatement(sql);
            
            pstm.setString(1, jUserNameField.getText());
            
            pstm.setString(2, new String(jPasswordField.getPassword()) );
            String d=jUserNameField.getText();
            ResultSet rs= pstm.executeQuery();
            if(rs.next())
            {
                JOptionPane.showConfirmDialog(null, "Welcome "+jUserNameField.getText(), "Login - Message", JOptionPane.PLAIN_MESSAGE);
                
                Date f=new Date(); 
                
                 
                new Logout(d,f).setVisible(true);        
                     
                dispose();
            }
            else
            {
                JOptionPane.showConfirmDialog(null, "Invalid Username or Password ", " Login - Message", JOptionPane.PLAIN_MESSAGE);
            }
            conn.close();
         }
        
            catch(Exception e)
             {
                 JOptionPane.showConfirmDialog(null, e);
             }

          
        }
    }//GEN-LAST:event_jSubmitButtonActionPerformed

